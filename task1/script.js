var p;
function init() {
    p = document.body.firstElementChild;
}

function ChangeFont(value) {
    p.style.fontFamily=value;
}

function ChangeAlign(value) {
    p.style.textAlign=value;
}

function ChangeHeight(value) {
    p.style.lineHeight=value;
}

function ChangeLSpace(value) {
    p.style.letterSpacing=value;
}

function ChangeIndent(value) {
    p.style.textIndent=value;
}

function ChangeTransform(value) {
    p.style.textTransform=value;
}

function ChangeDecorate(value) {
    p.style.textDecoration = value;
}

function ChangeBorder(value) {
    p.style.border = value;
}

function ChangeBorderColor(value) {
    p.style.borderColor = value;
}



